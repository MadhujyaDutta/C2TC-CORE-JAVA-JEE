package com.corejava;


public class Array_marks {

	public static void main(String[] args) {
		 
		int[] marks= new int[6];
		marks[0] = 95;
		marks[1] = 99;
		marks[2] = 98;
		marks[3] = 91;
		marks[4] = 90;
		marks[5] = 97;
		
		for(int i=0;i<6;i++) {
			System.out.println(marks[i]);
		}
	}

}
