package com.corejava;


public class SubMethod {

	public static void main(String[] args) {
		
		printFirstName();
		printLastName();

	}
	
	public static void printFirstName() {
		System.out.println("My First Name is Madhujya");
				
	}
	public static void printLastName() {
		System.out.println("My Last Name is Dutta");
	}
}
