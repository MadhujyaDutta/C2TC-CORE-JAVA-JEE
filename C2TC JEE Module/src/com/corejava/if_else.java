package com.corejava;


public class if_else {

	
		public static void main(String[] args) {
			boolean b=true;
			 if(b=false)
			  {
				System.out.println("Good Morning");
			  }
			 else
			  {
				System.out.println("Good Noon");
			  }
		}
}

/* 1.if(true) is valid empty statement.
 * 2. if, else can be used without else statement and without curly braces.
 * 3. Without curly braces only one statement is valid and that statement should be non declarative statement. */
 